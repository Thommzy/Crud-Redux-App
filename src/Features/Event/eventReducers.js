//Then we create our Reducer Functions

const createEvent = (state, payload) => {
  return [...state, payload.event];
}; // Here we did Pass the state and payload

// Second Function is To update the event

const updateEvent = (state, payload) => {
  return [
    ...state.filter(event => event.id !== payload.event.id),
    payload.event
  ];
};

const deleteEvent = (state, payload) => {
  return [...state.filter(event => event.id !== payload.eventId)];
};

// Then we export default createReducers ... then we pass our initial State from the const above..
// then we need to create the function Map
export default createReducer(initialState, {
  [CREATE_EVENT]: createEvent,
  [UPDATE_EVENT]: updateEvent,
  [DELETE_EVENT]: deleteEvent
});

